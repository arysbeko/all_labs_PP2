# Escaping allows the use of special characters in strings, such as:

# - **`\"`** — inserts a double quote inside a string.  
# - **`\'`** — inserts a single quote.  
# - **`\\`** — inserts a backslash.  
# - **`\n`** — creates a new line.  
# - **`\t`** — adds a tab space.  